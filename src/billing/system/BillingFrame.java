    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Main code upon ordering:
        String firstName1 = firstNameField.getText();
        String lastName1 = lastNameField.getText();
        String ccNumber1Str = ccNumberField.getText();
        String ccMonth1Str = ccMonthField.getText();
        String ccYear1Str = ccYearField.getText();
        String ccCVC1Str = ccCVCField.getText();
        int ccNumber1 = Integer.parseInt(ccNumber1Str);
        int ccMonth1 = Integer.parseInt(ccMonth1Str);
        int ccYear1 = Integer.parseInt(ccYear1Str);
        int ccCVC1 = Integer.parseInt(ccCVC1Str);
        Customer C1 = new Customer(firstName1,lastName1,ccNumber1,ccMonth1,ccYear1,ccCVC1);
        
        int Burg = (int) burgerSpinner.getValue();
        int HapMeal = (int) kbSpinner.getValue();
        int ChiNug = (int) cnSpinner.getValue();
        int Wm = (int) waterSpinner.getValue();
        int Cm = (int) cokeSpinner.getValue();
        int AJm = (int) ajSpinner.getValue();
        Order M1 = new Order(Burg,HapMeal,ChiNug,Wm,Cm,AJm);
        
        CustomerList.add(C1);
        Meals.add(M1);
        
        textArea.append(C1.toString());
        textArea.append(M1.toString());

    }//GEN-LAST:event_jButton1ActionPerformed

