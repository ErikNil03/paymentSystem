    private void lastNameFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lastNameFieldActionPerformed
        // TODO add your handling code here:
        for(int i=0;i<CustomerList.size();i++){
            if(firstNameField.getText().equals(CustomerList.get(i).firstName)){
                if(lastNameField.getText().equals(CustomerList.get(i).lastName)){
                    String cc1;
                    String cc2;
                    String cc3;
                    String cc4;
                    cc1 = String.valueOf(CustomerList.get(i).ccNumber);
                    cc2 = String.valueOf(CustomerList.get(i).ccMonth);
                    cc3 = String.valueOf(CustomerList.get(i).ccYear);
                    cc4 = String.valueOf(CustomerList.get(i).ccCVC);
                    ccNumberField.setText(cc1);
                    ccMonthField.setText(cc2);
                    ccYearField.setText(cc3);
                    ccCVCField.setText(cc4);
                    String me1;
                    String me2;
                    String me3;
                    String dr1;
                    String dr2;
                    String dr3;
                    me1 = String.valueOf(Meals.get(i).Burger);
                    me2 = String.valueOf(Meals.get(i).ChickenNuggets);
                    me3 = String.valueOf(Meals.get(i).KidsBurger);
                    dr1 = String.valueOf(Meals.get(i).Water);
                    dr2 = String.valueOf(Meals.get(i).Coke);
                    dr3 = String.valueOf(Meals.get(i).AppleJuice);
                    burgerSpinner.setValue(Integer.valueOf(me1));
                    cnSpinner.setValue(Integer.valueOf(me2));
                    kbSpinner.setValue(Integer.valueOf(me3));
                    waterSpinner.setValue(Integer.valueOf(dr1));
                    cokeSpinner.setValue(Integer.valueOf(dr2));
                    ajSpinner.setValue(Integer.valueOf(dr3));
                    
                }
            }
           
        }

    }//GEN-LAST:event_lastNameFieldActionPerformed

